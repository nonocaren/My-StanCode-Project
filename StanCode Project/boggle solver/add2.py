    cur_1 = l1
    cur_2 = l2
    lst_1 = []
    lst_2 = []

    first_ans = 0
    second_ans = 0
    final_ans = 0

    while cur_1.next is not None:
        lst_1.append(cur_1.val)
        cur_1 = cur_1.next
        lst_1.append(", ")

    for i in range(len(lst_1)):
        first_ans += lst_1[i] * 10**i

    while cur_2.next is not None:
        lst_2.append(cur_2.val)
        cur_2 = cur_2.next

